package ch1.lab4.autowire.byname;

public class Hostel {
	private String id;
	private String name;
	private String hostel;
	public String getId()
	{
		return id;
	}
	public void setId(String id)
	{
		this.id = id;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getHostel()
	{
		return hostel;
	}
	public void setHostel(String hostel)
	{
		this.hostel = hostel;
	}
}
